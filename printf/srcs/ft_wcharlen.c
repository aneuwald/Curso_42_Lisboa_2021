#include "ft_printf.h"

int		ft_wcharlen(wchar_t c)
{
	if (c < 0x80)
		return (1);
	if (c < 0x0800)
		return (2);
	if (c < 0x010000)
		return (3);
	if (c < 0x110000)
		return (4);
	return (0);
}
